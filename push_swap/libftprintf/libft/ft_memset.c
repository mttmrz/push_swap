#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	d;
	char	*s1;

	d = c;
	s1 = (char *) s;
	i = 0;
	while (i < n)
	{
		s1[i] = d;
		i++;
	}
	return (s1);
}
