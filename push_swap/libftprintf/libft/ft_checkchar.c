#include "libft.h"

int	ft_checkchar(char c, char const *s)
{
	int	i;
	int	check;

	check = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			check = 1;
		i++;
	}
	return (check);
}
